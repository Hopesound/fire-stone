import { readdirSync, readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { basename, join } from "node:path";

const heritageDir = "heritage";
const outputFile = "src/data/heritage-sites.generated.js";
const decoder = new TextDecoder("euc-kr");

const sourceNames = readdirSync(heritageDir)
  .filter((file) => file.endsWith(".shp"))
  .map((file) => basename(file, ".shp"))
  .sort();

const sites = [];
for (const sourceName of sourceNames) {
  const shpPath = join(heritageDir, `${sourceName}.shp`);
  const dbfPath = join(heritageDir, `${sourceName}.dbf`);
  const geometries = readShpCentroids(shpPath);
  const rows = readDbf(dbfPath);

  rows.forEach((row, index) => {
    const geometry = geometries[index];
    if (!geometry) {
      return;
    }
    const coordinate = kgd2002ToWgs84(geometry.x, geometry.y);
    const name = row["국가유산명"] || `${sourceName}-${index + 1}`;
    const code = row["유산코드"] || `${sourceName}-${index + 1}`;
    const areaM2 = Number(row["면적"] || 0);
    const region = [row["시도명"], row["시군구명"]].filter(Boolean).join(" ");
    const isProtectionZone = sourceName.includes("보호구역");

    sites.push({
      id: `${slug(sourceName)}-${code}-${index + 1}`,
      heritageCode: code,
      name,
      type: inferType(name, row["종목명"], isProtectionZone),
      designation: row["종목명"] || sourceName,
      sourceLayer: sourceName,
      isProtectionZone,
      region,
      lat: round(coordinate.lat, 7),
      lng: round(coordinate.lng, 7),
      areaM2,
      protectedRadiusKm: inferProtectedRadiusKm(areaM2, isProtectionZone),
      inscriptionYear: null,
      manager: row["시군구명"] ? `${row["시군구명"]} 문화유산 담당부서` : "문화유산 담당부서"
    });
  });
}

mkdirSync("src/data", { recursive: true });
writeFileSync(
  outputFile,
  `// Auto-generated by tools/convert-heritage.mjs from files in /heritage.\n` +
    `// Generated records: ${sites.length}\n` +
    `export const generatedHeritageSites = ${JSON.stringify(sites, null, 2)};\n`,
  "utf8"
);

console.log(`Generated ${sites.length} heritage records -> ${outputFile}`);

function readShpCentroids(filePath) {
  const buffer = readFileSync(filePath);
  const records = [];
  let offset = 100;

  while (offset + 8 <= buffer.length) {
    const contentLengthBytes = buffer.readInt32BE(offset + 4) * 2;
    const contentOffset = offset + 8;
    const shapeType = buffer.readInt32LE(contentOffset);

    if (shapeType === 0) {
      records.push(null);
    } else if (shapeType === 5 || shapeType === 3) {
      const xmin = buffer.readDoubleLE(contentOffset + 4);
      const ymin = buffer.readDoubleLE(contentOffset + 12);
      const xmax = buffer.readDoubleLE(contentOffset + 20);
      const ymax = buffer.readDoubleLE(contentOffset + 28);
      records.push({ x: (xmin + xmax) / 2, y: (ymin + ymax) / 2 });
    } else if (shapeType === 1) {
      records.push({
        x: buffer.readDoubleLE(contentOffset + 4),
        y: buffer.readDoubleLE(contentOffset + 12)
      });
    } else {
      records.push(null);
    }

    offset += 8 + contentLengthBytes;
  }

  return records;
}

function readDbf(filePath) {
  const buffer = readFileSync(filePath);
  const recordCount = buffer.readUInt32LE(4);
  const headerLength = buffer.readUInt16LE(8);
  const recordLength = buffer.readUInt16LE(10);
  const fields = [];

  for (let offset = 32; buffer[offset] !== 0x0d; offset += 32) {
    const name = decoder.decode(buffer.subarray(offset, offset + 11)).replace(/\0.*$/, "").trim();
    fields.push({
      name,
      type: String.fromCharCode(buffer[offset + 11]),
      length: buffer[offset + 16]
    });
  }

  const rows = [];
  for (let index = 0; index < recordCount; index += 1) {
    const offset = headerLength + index * recordLength;
    if (buffer[offset] === 0x2a) {
      rows.push({});
      continue;
    }
    let cursor = offset + 1;
    const row = {};
    fields.forEach((field) => {
      const value = decoder.decode(buffer.subarray(cursor, cursor + field.length)).trim();
      row[field.name] = value;
      cursor += field.length;
    });
    rows.push(row);
  }

  return rows;
}

function kgd2002ToWgs84(x, y) {
  const a = 6378137.0;
  const f = 1 / 298.257222101;
  const e2 = f * (2 - f);
  const ep2 = e2 / (1 - e2);
  const k0 = 0.9996;
  const lat0 = toRadians(38.0);
  const lon0 = toRadians(127.5);
  const falseEasting = 1000000.0;
  const falseNorthing = 2000000.0;

  const m0 = meridionalArc(lat0, a, e2);
  const m = m0 + (y - falseNorthing) / k0;
  const mu = m / (a * (1 - e2 / 4 - (3 * e2 * e2) / 64 - (5 * e2 * e2 * e2) / 256));
  const e1 = (1 - Math.sqrt(1 - e2)) / (1 + Math.sqrt(1 - e2));

  const phi1 =
    mu +
    (3 * e1) / 2 * Math.sin(2 * mu) -
    (27 * Math.pow(e1, 3)) / 32 * Math.sin(2 * mu) +
    (21 * e1 * e1) / 16 * Math.sin(4 * mu) -
    (55 * Math.pow(e1, 4)) / 32 * Math.sin(4 * mu) +
    (151 * Math.pow(e1, 3)) / 96 * Math.sin(6 * mu) +
    (1097 * Math.pow(e1, 4)) / 512 * Math.sin(8 * mu);

  const sinPhi1 = Math.sin(phi1);
  const cosPhi1 = Math.cos(phi1);
  const tanPhi1 = Math.tan(phi1);
  const c1 = ep2 * cosPhi1 * cosPhi1;
  const t1 = tanPhi1 * tanPhi1;
  const n1 = a / Math.sqrt(1 - e2 * sinPhi1 * sinPhi1);
  const r1 = (a * (1 - e2)) / Math.pow(1 - e2 * sinPhi1 * sinPhi1, 1.5);
  const d = (x - falseEasting) / (n1 * k0);

  const lat =
    phi1 -
    ((n1 * tanPhi1) / r1) *
      ((d * d) / 2 -
        ((5 + 3 * t1 + 10 * c1 - 4 * c1 * c1 - 9 * ep2) * Math.pow(d, 4)) / 24 +
        ((61 + 90 * t1 + 298 * c1 + 45 * t1 * t1 - 252 * ep2 - 3 * c1 * c1) * Math.pow(d, 6)) / 720);

  const lon =
    lon0 +
    (d -
      ((1 + 2 * t1 + c1) * Math.pow(d, 3)) / 6 +
      ((5 - 2 * c1 + 28 * t1 - 3 * c1 * c1 + 8 * ep2 + 24 * t1 * t1) * Math.pow(d, 5)) / 120) /
      cosPhi1;

  return {
    lat: toDegrees(lat),
    lng: toDegrees(lon)
  };
}

function meridionalArc(lat, a, e2) {
  return (
    a *
    ((1 - e2 / 4 - (3 * e2 * e2) / 64 - (5 * e2 * e2 * e2) / 256) * lat -
      ((3 * e2) / 8 + (3 * e2 * e2) / 32 + (45 * e2 * e2 * e2) / 1024) * Math.sin(2 * lat) +
      ((15 * e2 * e2) / 256 + (45 * e2 * e2 * e2) / 1024) * Math.sin(4 * lat) -
      ((35 * e2 * e2 * e2) / 3072) * Math.sin(6 * lat))
  );
}

function inferType(name, designation, isProtectionZone) {
  const text = `${name} ${designation}`;
  if (/사|암|사지|탑|불|석조|마애|불상|보살/.test(text)) {
    return "temple";
  }
  if (/고택|종택|생가|가옥|고가|한옥|민가|마을|서원|향교/.test(text)) {
    return "house";
  }
  return isProtectionZone ? "heritage" : "heritage";
}

function inferProtectedRadiusKm(areaM2, isProtectionZone) {
  if (!areaM2 || areaM2 <= 0) {
    return isProtectionZone ? 1 : 0.5;
  }
  const radius = Math.sqrt(areaM2 / Math.PI) / 1000;
  return round(Math.max(0.3, Math.min(5, radius)), 2);
}

function slug(value) {
  return value.replace(/[^\p{Letter}\p{Number}]+/gu, "-").replace(/^-|-$/g, "");
}

function round(value, digits) {
  const factor = 10 ** digits;
  return Math.round(value * factor) / factor;
}

function toRadians(value) {
  return (value * Math.PI) / 180;
}

function toDegrees(value) {
  return (value * 180) / Math.PI;
}
