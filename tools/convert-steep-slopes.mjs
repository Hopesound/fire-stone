function parseCsv(text) {
  const rows = [];
  let row = [];
  let value = "";
  let quoted = false;

  for (let index = 0; index < text.length; index += 1) {
    const char = text[index];
    const next = text[index + 1];

    if (char === "\"") {
      if (quoted && next === "\"") {
        value += "\"";
        index += 1;
      } else {
        quoted = !quoted;
      }
      continue;
    }

    if (char === "," && !quoted) {
      row.push(value);
      value = "";
      continue;
    }

    if ((char === "\n" || char === "\r") && !quoted) {
      if (char === "\r" && next === "\n") {
        index += 1;
      }
      row.push(value);
      rows.push(row);
      row = [];
      value = "";
      continue;
    }

    value += char;
  }

  if (value || row.length) {
    row.push(value);
    rows.push(row);
  }

  return rows;
}

function extractCity(address, manager) {
  const addressParts = address.split(/\s+/).filter(Boolean);
  const firstAddressPart = addressParts[0] || "";
  const secondAddressPart = addressParts[1] || "";
  if ((firstAddressPart === "충남" || firstAddressPart === "충청남도") && CITY_CENTERS[secondAddressPart]) {
    return secondAddressPart;
  }
  if (CITY_CENTERS[firstAddressPart]) {
    return firstAddressPart;
  }
  if (CITY_CENTERS[secondAddressPart]) {
    return secondAddressPart;
  }
  if (CITY_CENTERS[manager]) {
    return manager;
  }
  return "충남";
}

function buildOffset(seed) {
  const hash = hashString(seed);
  const angle = ((hash % 3600) / 3600) * Math.PI * 2;
  const ring = ((Math.floor(hash / 3600) % 1000) / 1000) ** 0.5;
  const distance = 0.012 + ring * 0.045;
  return {
    lat: Math.sin(angle) * distance,
    lng: Math.cos(angle) * distance
  };
}

function hashString(value) {
  let hash = 2166136261;
  for (let index = 0; index < value.length; index += 1) {
    hash ^= value.charCodeAt(index);
    hash = Math.imul(hash, 16777619);
  }
  return hash >>> 0;
}

function cleanText(value) {
  return String(value || "").replace(/\u0000/g, "").replace(/\s+/g, " ").trim();
}

function roundCoord(value) {
  return Math.round(value * 1000000) / 1000000;
}
